public abstract class DigitalSignature {
  protected NObject javonetHandle;

  public DigitalSignature(NObject handle) {
    this.javonetHandle = handle;
  }

  public void setJavonetHandle(NObject handle) {
    this.javonetHandle = handle;
  }
  /** Method */

  public java.lang.Boolean Verify(java.lang.Byte[] input, java.lang.Byte[] signature) {
    try {
      java.lang.Boolean res =
          javonetHandle.invoke("Verify", new Object[] {input}, new Object[] {signature});
      if (res == null) return false;
      return res;
    } catch (JavonetException _javonetException) {
      _javonetException.printStackTrace();
      return false;
    }
  }
  /** Method */

  public java.lang.Byte[] Sign(java.lang.Byte[] input, Class<?> returnArrayType) {
    try {
      Object[] res = javonetHandle.invoke("Sign", new Object[] {input});
      if (res == null) return null;
      return (java.lang.Byte[])
          Helper.ConvertNObjectToDestinationType((Object) res, returnArrayType);
    } catch (JavonetException _javonetException) {
      _javonetException.printStackTrace();
      return null;
    }
  }

  static {
    try {
      Activation.initializeJavonet();
    } catch (java.lang.Exception e) {
      e.printStackTrace();
    }
  }
}
