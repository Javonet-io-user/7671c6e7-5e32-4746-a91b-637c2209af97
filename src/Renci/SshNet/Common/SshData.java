public abstract class SshData {
  protected NObject javonetHandle;

  public SshData(NObject handle) {
    this.javonetHandle = handle;
  }

  public void setJavonetHandle(NObject handle) {
    this.javonetHandle = handle;
  }
  /** Method */

  public java.lang.Byte[] GetBytes(Class<?> returnArrayType) {
    try {
      Object[] res = javonetHandle.invoke("GetBytes");
      if (res == null) return null;
      return (java.lang.Byte[])
          Helper.ConvertNObjectToDestinationType((Object) res, returnArrayType);
    } catch (JavonetException _javonetException) {
      _javonetException.printStackTrace();
      return null;
    }
  }
  /** Method */

  public void Load(java.lang.Byte[] value) {
    try {
      javonetHandle.invoke("Load", new Object[] {value});
    } catch (JavonetException _javonetException) {
      _javonetException.printStackTrace();
    }
  }
  /** Method */

  public void Load(java.lang.Byte[] value, java.lang.Integer offset) {
    try {
      javonetHandle.invoke("Load", new Object[] {value}, offset);
    } catch (JavonetException _javonetException) {
      _javonetException.printStackTrace();
    }
  }

  static {
    try {
      Activation.initializeJavonet();
    } catch (java.lang.Exception e) {
      e.printStackTrace();
    }
  }
}
